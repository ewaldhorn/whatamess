void main() {
    var num1 = 5;
    var num2 = 7;
    // If
    if (num1 > num2) {
        print("num1 is bigger than num2");
    }

    if (num1 < num2) {
        print("num1 is smaller than num2");
    }

    // If Else
    if (num1 > num2) {
        print("num1 is bigger than num2");
    } else {
        print("num1 is smaller than num2");
    }

    // If Else If
    if (num1 > num2) {
        print("num1 is bigger than num2");
    } else if (num1 < num2) {
        print("num1 is smaller than num2");
    } else {
        print("num1 is equal to num2");
    }
}