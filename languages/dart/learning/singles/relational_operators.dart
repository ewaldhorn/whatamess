void main() {
  /*
    Arithmetic + - / * % 
    Relational < > <= >= == !=
    Assignment = += -= *= /= %=
    Type is as
    Logical && || !
    */

  var one = 200, two = 400;

  print(one == two); // False
  print(one != two); //True
  print(one > two); // False
  print(one < two); // True
  print(one >= two); // False
  print(one <= two); // True
}
