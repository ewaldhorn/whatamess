package main

import "fmt"

// -------------------------------------------------------------------------------------------------
type Updater interface {
	Update()
}

// -------------------------------------------------------------------------------------------------
type Transform struct {
	position int
}

// -------------------------------------------------------------------------------------------------
type Enemy struct {
	Transform
}

func (e *Enemy) Update() {
	e.position += 1
}

func (e *Enemy) Report() {
	fmt.Printf("We are at position %d.\n", e.position)
}

// -------------------------------------------------------------------------------------------------
func Update(u Updater) {
	u.Update()
}

// -------------------------------------------------------------------------------------------------

// -------------------------------------------------------------------------------------------------
func main() {
	e := &Enemy{}
	for i := 0; i < 100; i++ {
		Update(e)
	}
	e.Report()
}
